    private void dpTanggalAkhirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dpTanggalAkhirActionPerformed
        String tanggalMulai = DateHelper.dateToString(dpTanggalMulai.getDate());
        String tanggalAkhir = DateHelper.dateToString(dpTanggalAkhir.getDate());

        Transaksi transaksi = new Transaksi();
        transaksi.setHargaSewa(Integer.parseInt(etHarga.getText()));

        transaksi.setWaktuMulai(tanggalMulai);
        transaksi.setWaktuSelesai(tanggalAkhir);

        try {
            int totalHarga = transaksi.getTotalHarga();
            tvBiaya.setText(Integer.toString(totalHarga));
        } catch (ParseException ex) {
            Logger.getLogger(TambahTransaksiFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_dpTanggalAkhirActionPerformed

    private void btTambahTransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTambahTransaksiActionPerformed

        if ("".equals(ktpSelected) || "".equals(nopolSelected)) {
            JOptionPane.showMessageDialog(
                    null, "Harap memilih pelanggan dan mobil terlebih dahulu!"
            );
            return;
        }

        insertToTransaksi();
        updateMobilStatus();
        dispose();
    }//GEN-LAST:event_btTambahTransaksiActionPerformed

    private void insertToTransaksi() {

        try {
            Koneksi koneksi = new Koneksi();
            Connection conn = koneksi.getConnection();

            PreparedStatement ps;
            String executeQuery = "insert into sewa "
                    + "(ktp,nopol,harga_sewa,waktu_mulai,waktu_selesai) "
                    + "values(?,?,?,?,?)";
            ps = conn.prepareStatement(executeQuery);
            ps.setString(1, ktpSelected);
            ps.setString(2, nopolSelected);
            ps.setInt(3, Integer.parseInt(etHarga.getText()));
            ps.setString(4, DateHelper.dateToString(dpTanggalMulai.getDate()));
            ps.setString(5, DateHelper.dateToString(dpTanggalAkhir.getDate()));

            ps.executeUpdate();
        } catch (SQLException ex) {
            System.err.println(ex);
        }
    }

    private void updateMobilStatus() {
        try {
            Koneksi koneksi = new Koneksi();
            Connection conn = koneksi.getConnection();

            PreparedStatement ps;
            String executeQuery = "update mobil set "
                    + "status=? "
                    + "WHERE nopol=?";
            ps = conn.prepareStatement(executeQuery);
            ps.setBoolean(1, false);
            ps.setString(2, nopolSelected);

            ps.executeUpdate();
        } catch (SQLException ex) {
            System.err.println(ex);
        }
    }

