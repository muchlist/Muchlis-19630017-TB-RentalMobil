    public ArrayList<Mobil> getMobilList() {
        ArrayList<Mobil> mobilList = new ArrayList<>();
        Koneksi koneksi = new Koneksi();
        Connection connection = koneksi.getConnection();

        String query = "SELECT * FROM mobil WHERE status = 1 ORDER BY merk";
        Statement st;
        ResultSet rs;

        try {
            st = connection.createStatement();
            rs = st.executeQuery(query);
            while (rs.next()) {
                Mobil mobil = new Mobil(
                        rs.getString("nopol"),
                        rs.getString("merk"),
                        rs.getString("warna"),
                        rs.getInt("tahun"),
                        rs.getInt("harga_sewa"),
                        rs.getBoolean("status"));
                mobilList.add(mobil);
            }
        } catch (SQLException | NullPointerException ex) {
            System.out.println("Gagal melakukan query : " + ex.getMessage());
        }
        return mobilList;
    }

    private void loadMobilCombo() {
        cbMobil.removeAllItems();
        List<Mobil> mobilList = getMobilList();
        for (Mobil mbl : mobilList) {
            cbMobil.addItem(mbl);
        }
    }

